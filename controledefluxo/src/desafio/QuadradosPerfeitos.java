

import java.util.Scanner;

public class QuadradosPerfeitos {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        int[] d = new int[n + 1];

        for(int i = 0; i < d.length; i++) System.out.println(d[i]);
        

        sc.close();
    }
}
